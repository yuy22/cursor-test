# ============================================================
#  读 MD 文件
# ============================================================
MD_PATH = r"C:\Users\b886855456ly\Desktop\北师大版4年级数学下册教师用书(1)_RAG优化_base64.md"
text = open(MD_PATH, encoding='utf-8').read()
lines = text.split('\n')

# 收集所有标题
headings = []
for i, line in enumerate(lines):
    m = re.match(r'^(#{1,5}) (.+)', line)
    if m:
        level = len(m.group(1))
        title = m.group(2).strip()
        headings.append((i, level, title))

# ============================================================
#  为每个课时查找匹配的标题
# ============================================================
for unit, lesson in LESSONS:
    # 核心关键词（去掉括号内容做模糊匹配）
    core = re.sub(r'（[^）]*）', '', lesson).strip()

    matches = []
    for line_idx, level, title in headings:
        if core in title:
            matches.append((line_idx + 1, level, title))

    if matches:
        print(f'[{unit}] {lesson}')
        for ln, lv, t in matches:
            print(f'  行{ln:5d}  H{lv}  {t[:80]}')
    else:
        print(f'[{unit}] {lesson}  ← ❌ 未匹配')
    print()
