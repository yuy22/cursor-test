# 格式：在指定文本行 after/before 插入内容

def insert_after(text, anchor, content):
    """在 anchor 行之后插入 content"""
    idx = text.find(anchor)
    if idx == -1:
        print(f"  ⚠ 未找到锚点: {anchor[:40]}")
        return text
    end = text.find('\n', idx)
    return text[:end] + '\n' + content + text[end:]

def replace_line(text, old, new):
    return text.replace(old, new, 1)

